package day2;

/*
 * Calculate the Square of numbers from 0 to 9 and store in an Array
 */
public class SquareofNumbers {

	public static void main(String[] args) {
		
		int square[] = new int [10];
		square[0]= 0*0;
		square[1]=1*1;
		square[2]=2*2;
		square[3]=3*3;
		
		System.out.println(square[3]);
	}
}
